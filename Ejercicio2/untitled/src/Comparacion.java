public class Comparacion{
    public void comparacionesMatematicas(int valorUno, int valorDos){
        System.out.println("¿" +valorUno+ " es mayor que " +valorDos + "? " + (valorUno>valorDos));
        System.out.println("¿" +valorUno+ " es menor que " +valorDos + "? " + (valorUno<valorDos));
        System.out.println("¿" +valorUno+ " es igual que " +valorDos + "? " + (valorUno==valorDos));
        System.out.println("¿" +valorUno+ " es diferente que " +valorDos + "? " + (valorUno!=valorDos));
        System.out.println("¿" +valorUno+ " es mayor o igual que " +valorDos + "? " + (valorUno>=valorDos));
        System.out.println("¿" +valorUno+ " es menor o igual que " +valorDos + "? " + (valorUno<=valorDos));

    }
}